    //keypressed
    void checkKeyPressed(JTextField txt, java.awt.event.KeyEvent evt) {
        String value = txt.getText();
        int l = value.length();
        if (evt.getKeyChar() == '\b' || (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9' && l < 10)) {
            txt.setEditable(true);
        } else {
            txt.setEditable(false);
        }
    }

