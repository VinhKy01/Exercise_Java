    boolean kiemTraRong(JPanel panel) {
        for (Component c : panel.getComponents()) {
            if (c instanceof JTextField) {
                if (((JTextField) c).getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Vui lòng không để trống!");
                    return false;
                }
            }

        }
        return true;
    }
    
