    private void TableNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableNhanVienMouseClicked
        // TODO add your handling code here:
        int dong = TableNhanVien.getSelectedRow();
        TableModel model = TableNhanVien.getModel();

        txtMaso.setText(model.getValueAt(dong, 0).toString());
        txtTen.setText(model.getValueAt(dong, 1).toString());
        txtDiaChi.setText(model.getValueAt(dong, 3).toString());
        txtSDT.setText(model.getValueAt(dong, 5).toString());
        txtLuong.setText(model.getValueAt(dong, 6).toString());
        txtHSLuong.setText(model.getValueAt(dong, 7).toString());

        if (model.getValueAt(dong, 2).equals("Nam")) {
            rdbNam.setSelected(true);
        } else {
            rdbNu.setSelected(true);
        }

        String dateString = model.getValueAt(dong, 4).toString();

        Date date = null;
        try {
            date = (Date) new SimpleDateFormat("yyyy-MM-dd").parse(dateString);
        } catch (ParseException ex) {
            Logger.getLogger(fNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }

        dpNgaySinh.setDate(date);
    }//GEN-LAST:event_TableNhanVienMouseClicked

