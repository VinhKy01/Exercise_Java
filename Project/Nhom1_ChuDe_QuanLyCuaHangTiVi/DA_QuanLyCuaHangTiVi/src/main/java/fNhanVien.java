    void ThemNhanVien() {
        if (!kiemTraRong(pnlTT)) {
            return;
        }
        String msnvString, hotenString, diachiString, gioitinhString, sdtString;
        String ngaySinh;
        int luong = 0;
        float HSLuong = 0;

        msnvString = txtMaso.getText();

        hotenString = txtTen.getText();

        if (rdbNam.isSelected()) {
            gioitinhString = "Nam";
        } else {
            gioitinhString = "Nữ";
        }

        diachiString = txtDiaChi.getText();

        sdtString = txtSDT.getText();

        SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");
        ngaySinh = formater.format(dpNgaySinh.getDate());

        try {
            luong = Integer.parseInt(txtLuong.getText());

            HSLuong = Float.parseFloat(txtHSLuong.getText());
        } catch (Exception ex) {

        }

        if (!NhanVien.Instance().kiemTraNV(msnvString)) {
            NhanVien.Instance().ThemNV(msnvString, hotenString, gioitinhString, diachiString, ngaySinh, sdtString, luong, HSLuong);
        } else {
            JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại!");
        }

    }

