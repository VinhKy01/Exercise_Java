    void XoaNhanVien() {
        int dong = TableNhanVien.getSelectedRow();
        if (dong == -1) {
            JOptionPane.showMessageDialog(this, "Bạn có chọn dòng nào đâuuuuuuuu !");
            return;
        }
        TableModel model = TableNhanVien.getModel();

        String maso = model.getValueAt(dong, 0).toString();

        if (TaiKhoan.Instance().xoaTaiKhoan(maso) && NhanVien.Instance().xoaNhanVien(maso)) {
            JOptionPane.showMessageDialog(this, "Đã xóa thành công!");
        }

    }

    void SuaNhanVien() {
        int dong = TableNhanVien.getSelectedRow();
        if (dong == -1) {
            JOptionPane.showMessageDialog(this, "Bạn có chọn dòng nào đâuuuuuuuu !");
            return;
        }
        TableModel model = TableNhanVien.getModel();
        String maso = model.getValueAt(dong, 0).toString();

        String msnvString, hotenString, diachiString, gioitinhString, sdtString;
        String ngaySinh;
        int luong;
        float HSLuong;

        msnvString = txtMaso.getText();
        hotenString = txtTen.getText();
        if (rdbNam.isSelected()) {
            gioitinhString = "Nam";
        } else {
            gioitinhString = "Nữ";
        }
        diachiString = txtDiaChi.getText();
        sdtString = txtSDT.getText();

        SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");
        ngaySinh = formater.format(dpNgaySinh.getDate());

        luong = Integer.parseInt(txtLuong.getText());
        HSLuong = Float.parseFloat(txtHSLuong.getText());

        if (NhanVien.Instance().suaNhanVien(maso, msnvString, hotenString, gioitinhString, diachiString, ngaySinh, sdtString, luong, HSLuong)) {
            JOptionPane.showMessageDialog(this, "Đã sửa thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Sửa thất bại!");
        }
    }

