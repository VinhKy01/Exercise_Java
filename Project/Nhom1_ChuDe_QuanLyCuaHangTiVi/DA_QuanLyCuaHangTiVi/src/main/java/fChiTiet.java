    void LoadHeader(){
        tableChiTiet.getColumnModel().getColumn(0).setHeaderValue("Mã Tivi");
        tableChiTiet.getColumnModel().getColumn(1).setHeaderValue("Tên Tivi");
        tableChiTiet.getColumnModel().getColumn(2).setHeaderValue("Hãng");
        tableChiTiet.getColumnModel().getColumn(3).setHeaderValue("Kích thước");
        tableChiTiet.getColumnModel().getColumn(4).setHeaderValue("Tình trạng");
        tableChiTiet.getColumnModel().getColumn(5).setHeaderValue("Số lượng");
        tableChiTiet.getColumnModel().getColumn(6).setHeaderValue("Giá");
    }
    void LoadChiTiet() {

        tableChiTiet.setModel(TTDonHang.Instance().LoadChiTiet(idddonhang));

        DT_DonHang dh = DonHang.Instance().getDonHang(idddonhang);

        lbIDDonHang.setText("Mã đơn: " + dh.getIddonhang());
        lbNgayBan.setText("Ngày bán: " + dh.getNgayban());
        lbTongTien.setText("Tổng cộng: " + dh.getTongtien() + " (VNĐ)");
        lbTongTienBangChu.setText("Khuyến mãi: " + dh.getKhuyenmai() + " (%)");

        DT_KhachHang kh = KhachHang.Instance().getKHByID(dh.getMskh());
        lbTenKH.setText("Họ & tên khách hàng: " + kh.getTen());
        lbGTKH.setText("Giới tính: " + kh.getGioitinhString());
        lbNgaySinhKH.setText("Ngày sinh: " + kh.getNgaysinh());
        lbDCKH.setText("Địa chỉ: " + kh.getDiachi());
        lbSDTKH.setText("SĐT liên lạc: " + kh.getSdt());

        DT_NhanVien nv = NhanVien.Instance().getNVByID(dh.getMsnv());
        lbTenNV.setText("Nhân viên lập HD: " + nv.getHoten());

    }

