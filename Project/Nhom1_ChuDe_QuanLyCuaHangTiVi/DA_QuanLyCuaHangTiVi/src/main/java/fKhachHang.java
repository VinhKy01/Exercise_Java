    void LoadKhachHang() {
        TableStaff.setModel(KhachHang.Instance().LoadKhachHang());
        LoadHeader();
    }

    //Them khach hang
    void ThemKhachHang() {
        if(!kiemTraRong(pnlTT))
            return;
        
        String maso, ten, gioitinh, diachi, sdt, ngaysinh;

        maso = txtIdStaff.getText();
        ten = txtNameStaff.getText();

        if (rdbMale.isSelected()) {
            gioitinh = "Nam";
        } else {
            gioitinh = "Nữ";
        }
        diachi = txtAddressStaff.getText();
        sdt = txtNumberStaff.getText();

        SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");

        ngaysinh = formater.format(dpNgaySinh.getDate());

        if (KhachHang.Instance().ThemKhachHang(maso, ten, gioitinh, ngaysinh, diachi, sdt)) {
            JOptionPane.showMessageDialog(this, "Thêm khách hàng thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        }

    }

    //xoa khach hang
    void XoaKhachHang() {
        //lay ma khach hang
        int dong = TableStaff.getSelectedRow();
        if (dong == -1) {
            JOptionPane.showMessageDialog(this, "Bạn có chọn dòng nào đâuuuuuuuu !");
            return;
        }
        int n = JOptionPane.showConfirmDialog(
                this,
                "Việc xóa thông tin khách hàng sẽ xóa cả thông tin hóa đơn của khách hàng. Bạn có chắc muốn xóa?",
                "Thông báo",
                JOptionPane.OK_CANCEL_OPTION);

        TableModel model = TableStaff.getModel();

        String maso = model.getValueAt(dong, 0).toString();

        //xóa luôn hóa đơn
        if (n == JOptionPane.OK_OPTION) {
            if (KhachHang.Instance().XoaKHVaHD(maso)) {
                JOptionPane.showMessageDialog(this, "Xóa khách hàng thành công!");
            }
        }

    }

    //sua khach hang
    void SuaKhachHang() {
        //lay ma khach hang
        int dong = TableStaff.getSelectedRow();
        if (dong == -1) {
            JOptionPane.showMessageDialog(this, "Bạn có chọn dòng nào đâuuuuuuuu !");
            return;
        }
        TableModel model = TableStaff.getModel();

        String maso = model.getValueAt(dong, 0).toString();

        String masomoi, ten, gioitinh, diachi, sdt, ngaysinh;

        masomoi = txtIdStaff.getText();
        ten = txtNameStaff.getText();

        if (rdbMale.isSelected()) {
            gioitinh = "Nam";
        } else {
            gioitinh = "Nữ";
        }
        diachi = txtAddressStaff.getText();
        sdt = txtNumberStaff.getText();

        SimpleDateFormat formater = new SimpleDateFormat("yyyy-MM-dd");

        ngaysinh = formater.format(dpNgaySinh.getDate());

        //
        if (KhachHang.Instance().SuaKH(maso, masomoi, ten, gioitinh, ngaysinh, diachi, sdt)) {
            JOptionPane.showMessageDialog(this, "Đã sửa khách hàng!");
        }
    }
