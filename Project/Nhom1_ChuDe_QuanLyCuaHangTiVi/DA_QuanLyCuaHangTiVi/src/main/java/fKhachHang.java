    //check null
    boolean kiemTraRong(JPanel panel) {
        for (Component c : panel.getComponents()) {
            if (c instanceof JTextField) {
                if (((JTextField) c).getText().equals("")) {
                    JOptionPane.showMessageDialog(this, "Vui lòng không để trống!");
                    return false;
                }
            }

        }
        return true;
    }

    //tim kiem
    void TimKiem() {
        if (rdbMa.isSelected()) {
            TableStaff.setModel(KhachHang.Instance().TimKiemKH("mskh", txtTimKiem.getText()));
        } else {
            TableStaff.setModel(KhachHang.Instance().TimKiemKH("tenkh", txtTimKiem.getText()));
        }
    }

