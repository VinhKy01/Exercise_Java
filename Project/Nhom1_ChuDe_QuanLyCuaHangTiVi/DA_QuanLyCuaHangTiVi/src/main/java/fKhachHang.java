    private void txtNumberStaffKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumberStaffKeyPressed
        // TODO add your handling code here:
        String value = txtNumberStaff.getText();
        int l = value.length();
        if (evt.getKeyChar() == '\b' || (evt.getKeyChar() >= '0' && evt.getKeyChar() <= '9' && l < 10)) {
            txtNumberStaff.setEditable(true);
        } else {
            txtNumberStaff.setEditable(false);
        }
    }//GEN-LAST:event_txtNumberStaffKeyPressed

