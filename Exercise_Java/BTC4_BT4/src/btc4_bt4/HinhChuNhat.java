    private void figuresActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_figuresActionPerformed
        float tinhchuvi, tinhdientich, dai, rong;
            try {
                dai = Integer.parseInt(chieudai.getText());
                rong = Integer.parseInt(chieurong.getText());
                if (dai <= 0) {
                    throw new sobehon0(chieudai);
                }
                else{
                    if (rong <= 0 ) {
                        throw new sobehon0(chieurong);
                    }
                    else{
                        tinhchuvi = (Integer.parseInt(chieudai.getText()) + Integer.parseInt(chieurong.getText())) * 2;
                        tinhdientich = Integer.parseInt(chieudai.getText()) * Integer.parseInt(chieurong.getText());
                        chuvi.setText(tinhchuvi + "");
                        dientich.setText(tinhdientich + "");
                    }
                }
        }catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập vào một số !!!");
        }catch(sobehon0 ex){
            JOptionPane.showMessageDialog(rootPane, "Bạn phải nhập số lớn hơn 0");
            ex.getJTextField().selectAll();
            ex.getJTextField().requestFocus();
        }
        // TODO add your handling code here:
        
        // TODO add your handling code here:
        
    }//GEN-LAST:event_figuresActionPerformed

